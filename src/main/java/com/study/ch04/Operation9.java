package com.study.ch04;

public class Operation9 {
    public static void main(String[] args) {
        /*
        << 증강연산자>>

         */


        // 증감연산자.

        // ++은 1씩 증가 ++i 선증가 연산이라고 한다.
        // 선증가는 1이 바로 증가가 된다

        // 후증가는 1이 뒤에서 증가가 된다.
        // ++가 그다음번째에 적용이 되었다.
        // 후증가 위주로 쓰임.
        int i = 0;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(i--);
        System.out.println(i);



    }
}
